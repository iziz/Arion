test("Python runtime service calls ignore deprecated timeout environment settings", async () => {
  const server = await startJsonServer(async (_req) => {
    await delay(30);
    return {
      ok: true,
      result: {
        available: true,
        provider: "test-runtime"
      }
    };
  });
  const previous = snapshotEnv(["PYTHON_RUNTIME_SERVICE_URL", "PYTHON_RUNTIME_SERVICE_TIMEOUT_MS", "PYTHON_RUNTIME_SERVICE_ATTEMPTS"]);
  try {
    process.env.PYTHON_RUNTIME_SERVICE_URL = server.url;
    process.env.PYTHON_RUNTIME_SERVICE_TIMEOUT_MS = "1";
    process.env.PYTHON_RUNTIME_SERVICE_ATTEMPTS = "1";

    const result = await callPythonRuntimeService<{ available: boolean; provider: string }>("runtime", "/slow-runtime", {}, { metricKey: "test.runtime_boundary.python" });

    assert.deepEqual(result, { available: true, provider: "test-runtime" });
  } finally {
    restoreEnv(previous);
    await server.close();
  }
});

