test("long-running runtime boundaries do not reintroduce timeout cutoffs", async () => {
  const files = await collectRuntimeBoundaryFiles(process.cwd());
  const forbidden = [
    "AbortSignal.timeout",
    "PYTHON_RUNTIME_SERVICE_TIMEOUT",
    "WHISPER_TIMEOUT",
    "WHISPERX_TIMEOUT",
    "PADDLEOCR_TIMEOUT",
    "VLM_WORKER_TIMEOUT",
    "EMBEDDING_TIMEOUT",
    "VISUAL_EMBEDDING_TIMEOUT",
    "SCENE_TIMEOUT",
    "VISION_DETECTOR_TIMEOUT",
    "VISION_TRACKER_TIMEOUT",
    "SOCCERNET_ACTION_SPOTTING_TIMEOUT",
    "MODEL_DOCTOR_TIMEOUT",
    "safety limit"
  ];

  const violations: string[] = [];
  for (const file of files) {
    const text = await readFile(file, "utf8");
    for (const token of forbidden) {
      if (text.includes(token)) violations.push(`${path.relative(process.cwd(), file)} contains ${token}`);
    }
  }

  assert.deepEqual(violations, []);
});

